                        int lComun = Integer.parseInt(jTextFieldLitrosComun.getText().trim());
                        int lUltra = Integer.parseInt(jTextFieldLitrosUltra.getText().trim());
                        int lDeslactosada = Integer.parseInt(jTextFieldLitrosDeslactosada.getText().trim());
