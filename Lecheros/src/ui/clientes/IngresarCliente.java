            jTextFieldSucursalPS.setText(Integer.toString(getCliente().getSucursalPS()));
            if (getCliente().getGrupoCliente() == null) {
                jComboBoxGruposDeClientes.setSelectedIndex(0);
            } else {
                int indiceGrupoCliente = this.devolverIndiceGrupoDeCliente(getCliente().getGrupoCliente());
                jComboBoxGruposDeClientes.setSelectedIndex(indiceGrupoCliente);
            }
            int indiceReparto = this.devolverIndiceReparto(getCliente().getReparto());
            jComboBoxReparto.setSelectedIndex(indiceReparto);
            //jComboBoxReparto.setSelectedItem(getCliente().getReparto());
            jTextFieldNombre.setText(getCliente().getNombre());
            jTextFieldRazonSocial.setText(getCliente().getRazonSocial());
            jTextFieldRut.setText(getCliente().getRut());
