                jTextFieldLitrosComun.setText(Integer.toString(getCliente().getLitrosComun()));
                jTextFieldLitrosUltra.setText(Integer.toString(getCliente().getLitrosUltra()));
                jTextFieldLitrosDeslactosada.setText(Integer.toString(getCliente().getLitrosDeslactosada()));
                jTextFieldProductos.setText(Double.toString(getCliente().getProductos()));
