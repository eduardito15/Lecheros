    private int devolverIndiceReparto(Reparto reparto) {
        for (int i = 0; i < this.repartos.size(); i++) {
            Reparto r = this.repartos.get(i);
            if (r.equals(reparto)) {
                return i + 1;
            }
        }
        return 0;
    }

    private int devolverIndiceGrupoDeCliente(GrupoCliente grupoCliente) {
        for (int i = 0; i < this.grupoDeClientes.size(); i++) {
            GrupoCliente gc = this.grupoDeClientes.get(i);
            if (gc.equals(grupoCliente)) {
                return i + 1;
            }
        }
        return 0;
    }

    private void actualizarListaProductos() {
        if (cliente != null) {
            productosClienteProrrateo = cliente.getProductosClienteProrrateo();

            DefaultListModel modelo = new DefaultListModel();
            for (ProductoClienteProrrateo pcp : productosClienteProrrateo) {
                modelo.addElement(pcp);
            }

            jListProductos.setModel(modelo);
        } else {
            DefaultListModel modelo = new DefaultListModel();
            for (ProductoClienteProrrateo pcp : prodsCliNuevo) {
                modelo.addElement(pcp);
            }

            jListProductos.setModel(modelo);
        }
    }

