    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        if (jComboBoxReparto.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un reparto.", "Información", JOptionPane.INFORMATION_MESSAGE);
            jComboBoxReparto.requestFocus();
        } else {
            try {
                if (retencion == null) {
                    double importe = Double.parseDouble(jTextFieldImporte.getText().trim());

                    if (sisLiquidaciones.guardarRetencion((Reparto) jComboBoxReparto.getSelectedItem(), jTextFieldNombre.getText().trim(), jTextFieldAQueCorresponde.getText().trim(), importe)) {
                        JOptionPane.showMessageDialog(this, "Retención guardada correctamente.", "Información", JOptionPane.INFORMATION_MESSAGE);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error al guardar la Retención. ", "Error", JOptionPane.ERROR_MESSAGE);
                    }

                } else {
                    double importe = Double.parseDouble(jTextFieldImporte.getText().trim());
                    retencion.setNombre(jTextFieldNombre.getText().trim());
                    retencion.setaQueCorresponde(jTextFieldAQueCorresponde.getText().trim());
                    retencion.setImporte(importe);
                    if (sisLiquidaciones.actualizarRetencion(retencion)) {
                        JOptionPane.showMessageDialog(this, "Retención guardada correctamente.", "Información", JOptionPane.INFORMATION_MESSAGE);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error al guardar la Retención. ", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } catch (NumberFormatException ne) {
                JOptionPane.showMessageDialog(this, "El importe debe ser un numero.", "Información", JOptionPane.INFORMATION_MESSAGE);
                jTextFieldImporte.requestFocus();
                jTextFieldImporte.selectAll();
            } catch (Exception ex) {
                String stakTrace = util.Util.obtenerStackTraceEnString(ex);
                SistemaUsuarios.getInstance().registrarExcepcion(ex.toString(), stakTrace);
                JOptionPane.showMessageDialog(this, Constantes.MensajeDeErrorGenerico, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButtonGuardarActionPerformed

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        if ("Ver".equals(accion) && retencion != null) {
            jComboBoxReparto.setEnabled(false);
            jTextFieldNombre.setEditable(false);
            jTextFieldAQueCorresponde.setEditable(false);
            jTextFieldImporte.setEditable(false);
            jButtonGuardar.setEnabled(false);

            jTextFieldNombre.setText(retencion.getNombre());
            jTextFieldAQueCorresponde.setText(retencion.getaQueCorresponde());
            int indiceReparto = this.devolverIndiceReparto(retencion.getReparto());
            jComboBoxReparto.setSelectedIndex(indiceReparto);
            jTextFieldImporte.setText(Double.toString(retencion.getImporte()));
            jTextFieldImporte.requestFocus();
            jTextFieldImporte.selectAll();

            jButtonSalir.requestFocus();
        }
        if ("Modificar".equals(accion) && retencion != null) {
            jTextFieldNombre.setText(retencion.getNombre());
            jTextFieldAQueCorresponde.setText(retencion.getaQueCorresponde());
            int indiceReparto = this.devolverIndiceReparto(retencion.getReparto());
            jComboBoxReparto.setSelectedIndex(indiceReparto);
            jTextFieldImporte.setText(Double.toString(retencion.getImporte()));
            jTextFieldImporte.requestFocus();
            jTextFieldImporte.selectAll();
            jTextFieldImporte.requestFocus();
            jTextFieldImporte.selectAll();
        }
    }//GEN-LAST:event_formWindowActivated

    private int devolverIndiceReparto(Reparto reparto) {
        for (int i = 0; i < this.repartos.size(); i++) {
            Reparto r = this.repartos.get(i);
            if (r.equals(reparto)) {
                return i + 1;
            }
        }
        return 0;
    }

