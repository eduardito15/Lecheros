    private void jListDocumentosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListDocumentosMouseClicked
        // TODO add your handling code here:
        int seleccionado = jListDocumentos.getSelectedIndex();
        if (seleccionado != -1) {
            documentoDeVenta = docs.get(seleccionado);
            jTextFieldNombre.setText(documentoDeVenta.getTipoDocumento());
            jRadioButtonSuma.setSelected(documentoDeVenta.isSuma());
            jRadioButtonResta.setSelected(!documentoDeVenta.isSuma());
            jRadioButtonActivo.setSelected(documentoDeVenta.isActivo());
        }
    }//GEN-LAST:event_jListDocumentosMouseClicked

    
    private void actualizarListaDocumentosDeVenta(){
        docs = sis.devolverDocumentosDeVenta();

        DefaultListModel modelo = new DefaultListModel();
        for (DocumentoDeVenta dv : docs) {
            modelo.addElement(dv);
        }

        jListDocumentos.setModel(modelo);
    }
    
    
