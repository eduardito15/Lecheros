    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        if("".equals(jTextFieldNombre.getText().trim())){    
            //Es vacio el nombre
            JOptionPane.showMessageDialog(this, "El Documento de Venta debe tener un nombre.","Información",JOptionPane.INFORMATION_MESSAGE);
            jTextFieldNombre.requestFocus();
            jTextFieldNombre.selectAll();
        } else {
            try{
            //No es vacio el nombre
            if (jRadioButtonSuma.isSelected() || jRadioButtonResta.isSelected()) {
                //Hay seleccionado al menos una de las opciones de suma resta
                if(documentoDeVenta.getTipoDocumento() == null){
                    //Es Nuevo
                    DocumentoDeVenta aux = sis.devolverDocumentoDeVentaPorNombre(jTextFieldNombre.getText().trim());
                    if(aux == null){
                        if(sis.agregarDocumentoDeVenta(jTextFieldNombre.getText(), jRadioButtonSuma.isSelected())){
                            jTextFieldNombre.setText("");
                            actualizarListaDocumentosDeVenta();
                        } else {
                            JOptionPane.showMessageDialog(this, "Error al guardar el documento de Venta.","Error",JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Ya existe un Documento de Venta con ese nombre. Ingrese un nuevo nombre.", "Información", JOptionPane.INFORMATION_MESSAGE);
                        jTextFieldNombre.requestFocus();
                        jTextFieldNombre.selectAll();
                    }
                } else {
                    //Es uno ya existente.
                    if(documentoDeVenta.getTipoDocumento().equals(jTextFieldNombre.getText().trim())){
                        documentoDeVenta.setSuma(jRadioButtonSuma.isSelected());
                        documentoDeVenta.setActivo(jRadioButtonActivo.isSelected());
                        if (sis.actualizarDocumentoDeVenta(documentoDeVenta)) {
                            jTextFieldNombre.setText("");
                            actualizarListaDocumentosDeVenta();
                        } else {
                            JOptionPane.showMessageDialog(this, "Error al guardar el documento de Venta.", "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        DocumentoDeVenta aux = sis.devolverDocumentoDeVentaPorNombre(jTextFieldNombre.getText().trim());
                        if (aux == null) {
                            documentoDeVenta.setTipoDocumento(jTextFieldNombre.getText().trim());
                            documentoDeVenta.setSuma(jRadioButtonSuma.isSelected());
                            documentoDeVenta.setActivo(jRadioButtonActivo.isSelected());
                            if (sis.actualizarDocumentoDeVenta(documentoDeVenta)) {
                                jTextFieldNombre.setText("");
                                actualizarListaDocumentosDeVenta();
                            } else {
                                JOptionPane.showMessageDialog(this, "Error al guardar el documento de Venta.", "Error", JOptionPane.ERROR_MESSAGE);
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Ya existe un Documento de Venta con ese nombre. Ingrese un nuevo nombre.", "Información", JOptionPane.INFORMATION_MESSAGE);
                            jTextFieldNombre.requestFocus();
                            jTextFieldNombre.selectAll();
                        }
                    }
                }
            } else {
                //Ninguna de las opciones de suma resta esta seleccionada.
                JOptionPane.showMessageDialog(this, "Debes seleccionar si el documento suma o resta para la facturación.","Información",JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            String stakTrace = util.Util.obtenerStackTraceEnString(e);
            SistemaUsuarios.getInstance().registrarExcepcion(e.toString(), stakTrace);
            JOptionPane.showMessageDialog(this, Constantes.MensajeDeErrorGenerico, "Error", JOptionPane.ERROR_MESSAGE);
        } 
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

