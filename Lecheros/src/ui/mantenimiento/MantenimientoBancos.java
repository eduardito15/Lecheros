    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        if("".equals(jTextFieldNombre.getText().trim())) {
            JOptionPane.showMessageDialog(this, "El banco debe tener un nombre","Informaci贸n",JOptionPane.INFORMATION_MESSAGE);
            jTextFieldNombre.requestFocus();
            jTextFieldNombre.selectAll();
        } else if ("".equals(jTextFieldSucursal.getText().trim())) {
            JOptionPane.showMessageDialog(this, "El banco debe tener una sucursal","Informaci贸n",JOptionPane.INFORMATION_MESSAGE);
            jTextFieldSucursal.requestFocus();
            jTextFieldSucursal.selectAll();
        } else if ("".equals(jTextFieldDireccion.getText().trim())) {
            JOptionPane.showMessageDialog(this, "El banco debe tener una direccion","Informaci贸n",JOptionPane.INFORMATION_MESSAGE);
            jTextFieldDireccion.requestFocus();
            jTextFieldDireccion.selectAll();
        } else {
            try {
                // Todos los campos tienen valor
                if(banco.getName() == null) {
                    //Es uno nuevo
                    if(sis.agregarBanco(jTextFieldNombre.getText().trim(), jTextFieldSucursal.getText().trim(), jTextFieldDireccion.getText().trim())) {
                        vaciarCampos();
                        banco = new Banco();
                        actualizarListaBancos();
                    }
                } else {
                    //Es una actualizacion
                    banco.setName(jTextFieldNombre.getText().trim());
                    banco.setBranchOffice(jTextFieldSucursal.getText().trim());
                    banco.setAddress(jTextFieldDireccion.getText().trim());
                    if(sis.actualizarBanco(banco)) {
                        vaciarCampos();
                        banco = new Banco();
                        actualizarListaBancos();
                    }
                }
            } catch (Exception ex) {
                Logger.getLogger(MantenimientoBancos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

    private void jListBancosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListBancosMouseClicked
        // TODO add your handling code here:
        int seleccionado = jListBancos.getSelectedIndex();
        if(seleccionado != -1){
            banco = bancos.get(seleccionado);
            jTextFieldNombre.setText(banco.getName());
            jTextFieldSucursal.setText(banco.getBranchOffice());
            jTextFieldDireccion.setText(banco.getAddress());
        }
    }//GEN-LAST:event_jListBancosMouseClicked

    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        // TODO add your handling code here:
        if(banco.getName() == null){
            JOptionPane.showMessageDialog(this, "Para eliminar un banco, primero debes seleccionarlo de la lista.","Informaci贸n",JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                sis.eliminarBanco(banco);
                vaciarCampos();
                banco = new Banco();
                actualizarListaBancos();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Ocurrio un error al intentar eliminar el banco.","Error",JOptionPane.ERROR_MESSAGE);
        
            }
            
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    private void vaciarCampos() {
        jTextFieldNombre.setText("");
        jTextFieldSucursal.setText("");
        jTextFieldDireccion.setText("");
    }
