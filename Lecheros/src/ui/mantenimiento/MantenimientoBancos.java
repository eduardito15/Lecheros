    private void actualizarListaBancos() {
        bancos = sis.devolverBancos();
        
        DefaultListModel modelo = new DefaultListModel();
        for (Banco b : bancos) {
            modelo.addElement(b);
        }
        
        jListBancos.setModel(modelo);
    }

