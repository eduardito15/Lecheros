    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        if("".equals(jTextFieldNombreComision.getText().trim())){
            JOptionPane.showMessageDialog(this, "La comision debe tener un nombre.","Informaci贸n",JOptionPane.INFORMATION_MESSAGE);
            jTextFieldNombreComision.requestFocus();
            jTextFieldNombreComision.selectAll();
        } else {
            if("".equals(jTextFieldPorcentajeComision.getText().trim()) || "".equals(jTextFieldPorcentajeLeche.getText().trim())){
                JOptionPane.showMessageDialog(this, "La comision debe tener un porcentaje. Que debe ser un numero entero.","Informaci贸n",JOptionPane.INFORMATION_MESSAGE);
                jTextFieldPorcentajeComision.requestFocus();
                jTextFieldPorcentajeComision.selectAll();
            } else {
                try {
                    SistemaMantenimiento sm = SistemaMantenimiento.getInstance();
                    if (comision.getNombre() == null) {
                        double porcentaje = Double.parseDouble(jTextFieldPorcentajeComision.getText().trim());
                        double porcentajeLeche = Double.parseDouble(jTextFieldPorcentajeLeche.getText().trim());
                        if (sm.agregarComision(jTextFieldNombreComision.getText(), porcentaje, porcentajeLeche)) {
                            jTextFieldNombreComision.setText("");
                            jTextFieldPorcentajeComision.setText("");
                            jTextFieldPorcentajeLeche.setText("");
                            comision = new Comision();
                            actualizarListaComisiones();
                            jTextFieldNombreComision.requestFocus();
                            jTextFieldNombreComision.selectAll();
                        } else {

                        }
                    } else {
                        comision.setNombre(jTextFieldNombreComision.getText());
                        comision.setPorcentaje(Double.parseDouble(jTextFieldPorcentajeComision.getText().trim()));
                        comision.setPorcentajeLeche(Double.parseDouble(jTextFieldPorcentajeLeche.getText().trim()));
                        sis.actualizarComision(comision);
                        jTextFieldNombreComision.setText("");
                        jTextFieldPorcentajeComision.setText("");
                        jTextFieldPorcentajeLeche.setText("");
                        comision = new Comision();
                        actualizarListaComisiones();
                        jTextFieldNombreComision.requestFocus();
                        jTextFieldNombreComision.selectAll();
                    }
                } catch (NumberFormatException nfe) {
                    JOptionPane.showMessageDialog(this, "El porcentaje de comision debe ser un numero.", "Informaci贸n", JOptionPane.INFORMATION_MESSAGE);
                    jTextFieldPorcentajeComision.requestFocus();
                    jTextFieldPorcentajeComision.selectAll();
                } catch (Exception e) {
                    String stakTrace = util.Util.obtenerStackTraceEnString(e);
                    SistemaUsuarios.getInstance().registrarExcepcion(e.toString(), stakTrace);
                    JOptionPane.showMessageDialog(this, Constantes.MensajeDeErrorGenerico, "Error", JOptionPane.ERROR_MESSAGE);
                }   
                
            }
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

    private void jListComisionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListComisionesMouseClicked
        // TODO add your handling code here:
        int seleccionado = jListComisiones.getSelectedIndex();
        if(seleccionado != -1){
            comision = comisiones.get(seleccionado);
            jTextFieldNombreComision.setText(comision.getNombre());
            jTextFieldPorcentajeComision.setText(Double.toString(comision.getPorcentaje()));
            jTextFieldPorcentajeLeche.setText(Double.toString(comision.getPorcentajeLeche()));
        }
    }//GEN-LAST:event_jListComisionesMouseClicked

    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        // TODO add your handling code here:
        try{
            if (comision.getNombre() == null) {
                JOptionPane.showMessageDialog(this, "Para eliminar una comision, primero debes seleccionarla de la lista.", "Informaci贸n", JOptionPane.INFORMATION_MESSAGE);
            } else {
                sis.eliminarComision(comision);
                jTextFieldNombreComision.setText("");
                jTextFieldPorcentajeComision.setText("");
                jTextFieldPorcentajeLeche.setText("");
                comision = new Comision();
                actualizarListaComisiones();
            }
        }
        catch (HibernateException e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar la comision." + "\n\n" + e.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            String stakTrace = util.Util.obtenerStackTraceEnString(ex);
            SistemaUsuarios.getInstance().registrarExcepcion(ex.toString(), stakTrace);
            JOptionPane.showMessageDialog(this, "Error al eliminar la comision." + "\n\n" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

