    private void actualizarListaComisiones(){
        comisiones = sis.devolverComisiones();
        
        DefaultListModel modelo = new DefaultListModel();
        for (Comision c : comisiones) {
            modelo.addElement(c);
        }
        
        jListComisiones.setModel(modelo);
    }
