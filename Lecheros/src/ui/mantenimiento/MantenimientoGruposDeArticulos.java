    private void actualizarListaGruposDeArticulos() {
        gruposDeArticulos = sis.devolverGruposDeArticulos();

        DefaultListModel modelo = new DefaultListModel();
        for (GrupoDeArticulos gda : gruposDeArticulos) {
            modelo.addElement(gda);
        }

        jListGruposDeArticulos.setModel(modelo);
    }

    private void vaciarCampos() {
        jTextFieldNombreGrupoDeArticulos.setText("");
        jTextAreaCodigos.setText("");
    }

    private boolean codigoValido(String cod) {
        boolean retorno = true;
        for (GrupoDeArticulos gda : gruposDeArticulos) {
            if (gda.getNombre().equals(cod)) {
                retorno = false;
                break;
            }
        }
        return retorno;
    }

    public String armarStringCodigosArticulos(List<Articulo> articulos) {
        String retorno = "";
        int cant = 0;
        for (Articulo art : articulos) {
            if (cant == 0) {
                retorno = retorno + art.getCodigo();
            } else {
                if (cant == 5 || cant == 10 || cant == 15 || cant == 20) {
                    retorno = retorno + "\n";
                }
                retorno = retorno + ", " + art.getCodigo();
            }
            cant++;
        }
        return retorno;
    }
    
    public int devolverIndiceComboFacturarDeA() {
        int retorno = 1;
        int cantidadDeOpciones = jComboBoxFacturarDeA.getItemCount();
        for(int i = 0; i<cantidadDeOpciones; i++) {
            if(Integer.toString(grupoDeArticulos.getFacturarDeA()).equals(jComboBoxFacturarDeA.getItemAt(i))) {
                retorno = i;
            }
        }
        return retorno;
    }

