    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        if ("".equals(jTextFieldNombreGrupoDeArticulos.getText().trim())) {
            JOptionPane.showMessageDialog(this, "El grupo de articulos debe tener un nombre.", "Información", JOptionPane.INFORMATION_MESSAGE);
            jTextFieldNombreGrupoDeArticulos.requestFocus();
            jTextFieldNombreGrupoDeArticulos.selectAll();
        } else {
            try {
                //Verifico que no exista un grupo de cl con ese nombre
                if (grupoDeArticulos.getNombre() == null) {
                    //Aca se agrega un nuevo grupo de articulos
                    //Verifico que sea un nombre valido, que no este repetido
                    if (!codigoValido(jTextFieldNombreGrupoDeArticulos.getText())) {
                        JOptionPane.showMessageDialog(this, "Ya existe un grupo de articulos con ese nombre.", "Información", JOptionPane.INFORMATION_MESSAGE);
                        jTextFieldNombreGrupoDeArticulos.requestFocus();
                        jTextFieldNombreGrupoDeArticulos.selectAll();
                    } else if (sis.agregarGrupoDeArticulos(jTextFieldNombreGrupoDeArticulos.getText().trim(), articulos, Integer.parseInt((String)jComboBoxFacturarDeA.getSelectedItem()))) {
                        jTextFieldNombreGrupoDeArticulos.setText("");
                        jTextAreaCodigos.setText("");
                        jTextFieldCodigoArticulo.setText("");
                        jLabelDescripcion.setText("");
                        jComboBoxFacturarDeA.setSelectedIndex(0);
                        grupoDeArticulos = new GrupoDeArticulos();
                        articulos = new ArrayList<>();
                        actualizarListaGruposDeArticulos();
                        jTextFieldNombreGrupoDeArticulos.requestFocus();
                        jTextFieldNombreGrupoDeArticulos.selectAll();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error al guardar el grupo de articulos.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else //Aca se actualiza un grupo de clientes existente
                //Chequeo se quiere cambiar el nombre
                if (!grupoDeArticulos.getNombre().equals(jTextFieldNombreGrupoDeArticulos.getText())) {
                    //Si quiere cambiar el nombre, verifico que sea un nombre valido
                    if (!codigoValido(jTextFieldNombreGrupoDeArticulos.getText())) {
                        JOptionPane.showMessageDialog(this, "Ya existe un grupo de articulos con ese nombre.", "Información", JOptionPane.INFORMATION_MESSAGE);
                        jTextFieldNombreGrupoDeArticulos.requestFocus();
                        jTextFieldNombreGrupoDeArticulos.selectAll();
                    } else {
                        //Si el nombre es valido actualizo los datos
                        grupoDeArticulos.setNombre(jTextFieldNombreGrupoDeArticulos.getText());
                        grupoDeArticulos.setArticulos(articulos);
                        grupoDeArticulos.setFacturarDeA(Integer.parseInt((String)jComboBoxFacturarDeA.getSelectedItem()));
                        sis.actualizarGrupoDeArticulos(grupoDeArticulos);
                        jTextFieldNombreGrupoDeArticulos.setText("");
                        jTextAreaCodigos.setText("");
                        jTextFieldCodigoArticulo.setText("");
                        jLabelDescripcion.setText("");
                        jComboBoxFacturarDeA.setSelectedIndex(0);
                        grupoDeArticulos = new GrupoDeArticulos();
                        articulos = new ArrayList<>();
                        actualizarListaGruposDeArticulos();
                        jTextFieldNombreGrupoDeArticulos.requestFocus();
                        jTextFieldNombreGrupoDeArticulos.selectAll();
                    }
                } else {
                    //Si no quiere cambiar el nombre todo bien, se guarda con el mismo nombre
                    grupoDeArticulos.setNombre(jTextFieldNombreGrupoDeArticulos.getText());
                    grupoDeArticulos.setArticulos(articulos);
                    grupoDeArticulos.setFacturarDeA(Integer.parseInt((String)jComboBoxFacturarDeA.getSelectedItem()));
                    sis.actualizarGrupoDeArticulos(grupoDeArticulos);
                    jTextFieldNombreGrupoDeArticulos.setText("");
                    jTextAreaCodigos.setText("");
                    jTextFieldCodigoArticulo.setText("");
                    jLabelDescripcion.setText("");
                    jComboBoxFacturarDeA.setSelectedIndex(0);
                    grupoDeArticulos = new GrupoDeArticulos();
                    articulos = new ArrayList<>();
                    actualizarListaGruposDeArticulos();
                    jTextFieldNombreGrupoDeArticulos.requestFocus();
                    jTextFieldNombreGrupoDeArticulos.selectAll();
                }
            } catch (Exception e) {
                String stakTrace = util.Util.obtenerStackTraceEnString(e);
                SistemaUsuarios.getInstance().registrarExcepcion(e.toString(), stakTrace);
                JOptionPane.showMessageDialog(this, Constantes.MensajeDeErrorGenerico, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

    private void jTextFieldCodigoArticuloKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldCodigoArticuloKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            //jTextFieldCantidadRenglon.requestFocus();
            //jTextFieldCantidadRenglon.selectAll();
            if ("".equals(jTextFieldCodigoArticulo.getText().trim())) {
                jLabel12.setText("El codigo de producto no es valido.");
                jLabelDescripcion.setText("");
                jTextFieldCodigoArticulo.requestFocus();
                jTextFieldCodigoArticulo.selectAll();
            } else {
                try {
                    int codigo = Integer.parseInt(jTextFieldCodigoArticulo.getText());
                    a = sis.devolverArticuloPorCodigo(codigo);
                    if (a != null) {
                        jLabelDescripcion.setText(a.getDescripcion());
                        jLabel12.setText("");
                        if (articulos.contains(a)) {
                            jButtonQuitar.setVisible(true);
                            jButtonQuitar.requestFocus();
                        } else {
                            jButtonAgregar.requestFocus();
                        }
                    } else {
                        jLabel12.setText("El codigo de producto no es valido.");
                        jLabelDescripcion.setText("");
                        jTextFieldCodigoArticulo.requestFocus();
                        jTextFieldCodigoArticulo.selectAll();
                    }
                } catch (NumberFormatException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "El codigo debe ser un numero entero.", "Información", JOptionPane.INFORMATION_MESSAGE);
                    jTextFieldCodigoArticulo.requestFocus();
                    jTextFieldCodigoArticulo.selectAll();
                } catch (Exception e) {
                    String stakTrace = util.Util.obtenerStackTraceEnString(e);
                    SistemaUsuarios.getInstance().registrarExcepcion(e.toString(), stakTrace);
                    JOptionPane.showMessageDialog(this, Constantes.MensajeDeErrorGenerico, "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jTextFieldCodigoArticuloKeyPressed

