    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        try {
            String rutaArchivo = jTextFieldRutaArchivo.getText().trim();
            if ("".equals(rutaArchivo)) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un archivo para cargar los precios.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                jTextFieldRutaArchivo.requestFocus();
                jTextFieldRutaArchivo.selectAll();
            } else {
                int desdeFila = Integer.parseInt(jTextFieldDesdeFila.getText());
                int hastaFila = Integer.parseInt(jTextFieldHastaFila.getText());
                //Calendar fecha = dateChooserComboFecha.getSelectedDate();
                Date fecha = jDateChooserFecha.getDate();
                jTextFieldRutaArchivo.setEnabled(false);
                jButtonBuscar.setEnabled(false);
                jDateChooserFecha.setEnabled(false);
                jTextFieldDesdeFila.setEnabled(false);
                jTextFieldHastaFila.setEnabled(false);
                jLabelEspera.setVisible(true);
                Thread worker = new Thread() {
                    public void run() {
                        try {
                            sis.cargarPrecios(rutaArchivo, fecha, desdeFila, hastaFila);

                            SwingUtilities.invokeLater(new Runnable() {
                                public void run() {
                                    jLabelEspera.setVisible(false);
                                    JOptionPane.showMessageDialog(IngresoAutomaticoDePrecios.this, "Precios cargados correctamente.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                                    IngresoAutomaticoDePrecios.this.dispose();
                                }
                            });
                        } catch (Exception exp) {
                            jLabelEspera.setVisible(false);
                            String stakTrace = util.Util.obtenerStackTraceEnString(exp);
                            SistemaUsuarios.getInstance().registrarExcepcion(exp.toString(), stakTrace);
                            JOptionPane.showMessageDialog(IngresoAutomaticoDePrecios.this, Constantes.MensajeDeErrorGenerico, "Error", JOptionPane.ERROR_MESSAGE);
                            IngresoAutomaticoDePrecios.this.dispose();
                        }
                    }
                };
                worker.start();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Desde fila y hasta fila deben ser numeros. Que indican desde que fila y hasta que fila del excel leer.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
            jTextFieldDesdeFila.requestFocus();
            jTextFieldDesdeFila.selectAll();
        } catch (Exception e) {
            String stakTrace = util.Util.obtenerStackTraceEnString(e);
            SistemaUsuarios.getInstance().registrarExcepcion(e.toString(), stakTrace);
            JOptionPane.showMessageDialog(this, Constantes.MensajeDeErrorGenerico, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

